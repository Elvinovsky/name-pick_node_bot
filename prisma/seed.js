async function seed() {
  console.log('🌱 Начинаем заполнение базы...');
  // await prisma.name.deleteMany();

  const names = eroupOldNames.map((name) => ({
    name: name.name,
    origin: 'eroupean',
    gender: name.gender,
    category: 'classicOld',
    note: name.note,
  }));

  for (const nameData of names) {
    await prisma.name.upsert({
      where: { name: nameData.name },
      update: {
        origin: nameData.origin,
        gender: nameData.gender,
        category: nameData.category,
        note: nameData.note,
      },
      create: nameData,
    });
  }
  console.log('✅ Данные успешно добавлены!');
}

